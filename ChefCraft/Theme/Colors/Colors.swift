import UIKit

extension UIColor {
    @nonobjc class var black: UIColor {
        return UIColor(white: 51.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkGreen: UIColor {
        return UIColor(red: 73.0 / 255.0, green: 85.0 / 255.0, blue: 80 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var red: UIColor {
        return UIColor(red: 227.0 / 255.0, green: 88.0 / 255.0, blue: 88.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var unActive1: UIColor {
        return UIColor(red: 224.0 / 255.0, green: 224.0 / 255.0, blue: 224.0 / 255.0, alpha: 1.0)
    }
}
